CREATE or REPLACE TRIGGER TRIG_SEQ_POR_CIUDAD
BEFORE INSERT ON POR_CIUDAD
FOR EACH ROW
BEGIN
SELECT SEQ_POR_CIUDAD.nextval INTO :new.ID FROM dual;
END;
/

